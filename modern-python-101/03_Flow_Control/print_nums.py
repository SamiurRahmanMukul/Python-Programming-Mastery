"""
Louis wants to show people of Zortan 🪐 how to count from 0 to 9.
"""

# Using a built-in function `range`
for num in range(10):
    print(num)

print()

# ----------------------------------------------------
# Cece who is from Zortan loves the number 4!
# ----------------------------------------------------
for num in range(10):
    if num == 4:
        print("Cece loves the number 4!")
    else:
        print(num)

print()

# ----------------------------------------------------------------
# Zortans are not happy, they want to see numbers on the same line
# ----------------------------------------------------------------
for num in range(10):
    print(num, end=" ")

print()

# ------------------------------------------------------------------
# Cece doesn't like blank space, she wants an underscore `_` instead
# ------------------------------------------------------------------
for num in range(10):
    print(num, end="_")

print()

